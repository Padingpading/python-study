# 一、基本使用截取。
s = "python"
# 1. 截取[0,3)：包含索引0，不包含索引3（字符p,y,t）
print(s[0:3])  # 输出：pyt
# 2. 省略start：从开头（0）截取到end=4（不包含4）
print(s[:4])   # 输出：pyth
# 3. 省略end：从start=2截取到字符串结尾（包含最后一个字符）
print(s[2:])   # 输出：thon
# 4. 省略start和end：截取整个字符串（等效于复制字符串）
print(s[:])    # 输出：python
# 5. 反向索引：截取倒数3个字符（-3到结尾）
print(s[-3:])  # 输出：hon
# 6. 正向+反向索引混用：截取索引1到倒数2个字符（不包含倒数2）
print(s[1:-2]) # 输出：yth

# 二、步长截取。
s = "0123456789"  # 数字字符串，方便演示步长
# step=2：每隔1个字符截取（取索引0,2,4,6,8）
print(s[::2])  # 输出：02468
# step=3，指定start=1、end=8：从1开始到8结束，每隔2个截取
print(s[1:8:3])  # 输出：147（索引1、4、7）


# 三、应用场景。
s = "Hello Python!"  # 测试字符串
# 1. 取最后n个字符（例：最后6个）
print(s[-6:])  # 输出：Python!
# 2. 去掉前n个字符（例：去掉前5个）
print(s[5:])   # 输出： Python!（注意前面的空格）
# 3. 字符串完全反转
print(s[::-1]) # 输出：!nohtyP olleH
# 4. 间隔截取（偶数位/奇数位字符，从0开始计数）
nums = "0123456789"
print(nums[::2])  # 偶数位：02468
print(nums[1::2]) # 奇数位：13579
# 5. 去掉首尾各1个字符（截取中间部分）
print(s[1:-1])    # 输出：ello Python


