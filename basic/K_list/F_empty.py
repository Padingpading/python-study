# 初始化一个空列表
my_list = []

# 检查列表是否为空
# 由于my_list是空的，所以条件不满足，执行else分支
if my_list:
    print("列表不为空")  # 这行代码不会执行
else:
    print("列表为空")  # 这行代码会执行，输出“列表为空”

# 填充列表后再次检查
# 向my_list中添加元素1, 2, 3
my_list = [1, 2, 3]

# 再次检查列表是否为空
# 由于my_list现在不是空的，所以条件满足，执行if分支
if my_list:
    print("列表不为空")  # 这行代码会执行，输出“列表不为空”
else:
    print("列表为空")  # 这行代码不会执行
