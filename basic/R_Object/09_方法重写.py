#Person类
class Person:
    def __init__(self, name, age, gender):
        # 实例属性 属性名=属性值
        self.name = name
        self.age = age
        self.gender = gender
    def speak(self, msg):
        print(f'我叫{self.name} 年龄{self.age} 性别{self.gender} 我想说{msg}')
# 学生类
class Student(Person):
    # 继承父类的构造方法
    def __init__(self, name, age, gender,stu_id,grade):
        # 方式一、继承父类的构造方法
        super().__init__(name, age, gender)
        self.stu_id = stu_id
        self.grade = grade
    #方法重写
    def speak(self, msg):
        print("我是student的speak方法,进行重写")
        #调用父类
        super().speak(msg)

