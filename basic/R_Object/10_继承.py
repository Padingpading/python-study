#Person类
class Person:
    def __init__(self, name, age, gender):
        # 实例属性 属性名=属性值
        self.name = name
        self.age = age
        self.gender = gender
    def speak(self, msg):
        print(f'我叫{self.name} 年龄{self.age} 性别{self.gender} 我想说{msg}')

# 学生类
class Student(Person):
    # 继承父类的构造方法
    def __init__(self, name, age, gender,grade):
        # 方式一、继承父类的构造方法
        super().__init__(name, age, gender)
        # 方式二、self必须传递。
        Person.__init__(self, name, age, gender)
        self.grade = grade
    # 执行pass直接略过
    def speak(self, msg):
        print("我是student的speak方法")
    def study(self, msg):
        print(f"我要学习课程{msg}")
# 创建student的实例对象
s1 = Student('张三',18,'男',"初一")
print(s1.__dict__)
print(type(s1))
